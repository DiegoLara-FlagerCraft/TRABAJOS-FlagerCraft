Puntaje = int(input("Ingrese un numero entero: "))
if Puntaje >= 90:
    print("A")
else:
    if Puntaje >= 80:
        print("B")
    else:
        if Puntaje >= 70:
            print("C")
        else:
            if Puntaje >= 60:
                print("D")
            else:
                print("F")