# 4.Escribe un algoritmo o el respectivo diagrama de flujo para imprimir la suma, 
# resta, multiplicación, división y residuo de dos números dados.

print("Vamos a calcular la suma, la resta, la multiplicación, la división \n"
"y el residuo de dos numeros")
Num1 = float(input("Ingresa el primer numero: "))
Num2 = float(input("Ingresa el segundo numero: "))
Suma = Num1 + Num2
Resta = Num1 - Num2
Multiplicacion = Num1 * Num2
Division = Num1 / Num2
Residuo = Num1 % Num2
print("La Suma de los dos numeros ingresados es:",Suma)
print("La Resta de los dos numeros ingresados es:",Resta)
print("La Multiplicación de los dos numeros ingresados es:",Multiplicacion)
print("La División de los dos numeros ingresados es:",Division)
print("El Residuo de los dos numeros ingresados es:",Residuo)